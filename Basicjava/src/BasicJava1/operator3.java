package BasicJava1;

public class operator3 {
	public static void main(String[] args) {
		
		//&& operator AND
		System.out.println((5>3)&&(8>5)); //true
		System.out.println(5<3);//false
		
		//   || OR operator
		
		System.out.println(5>3);//true
		System.out.println((5<3)||(8>5));//true
		System.out.println((5<3)||(8<5));//false
		
		// ! NOT operator
		
		System.out.println(!(5==3));//true
		System.out.println(!(5>3));//false
	}

}
